package PatikaStore;

public class CellPhone extends Product{
    String memory;
    String screenSize;
    int batteryPower;
    int ram;
    String color;

    public CellPhone(int id, double unitPrice, double discountRate, int stockQuantity, String name, Brand brand, String memory, String screenSize, int batteryPower, int ram, String color) {
        super(id, unitPrice, discountRate, stockQuantity, name, brand);
        this.memory = memory;
        this.screenSize = screenSize;
        this.batteryPower = batteryPower;
        this.ram = ram;
        this.color = color;
    }

    public String getMemory() {
        return memory;
    }

    public void setMemory(String memory) {
        this.memory = memory;
    }

    public String getScreenSize() {
        return screenSize;
    }

    public void setScreenSize(String screenSize) {
        this.screenSize = screenSize;
    }

    public int getBatteryPower() {
        return batteryPower;
    }

    public void setBatteryPower(int batteryPower) {
        this.batteryPower = batteryPower;
    }

    public int getRam() {
        return ram;
    }

    public void setRam(int ram) {
        this.ram = ram;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
